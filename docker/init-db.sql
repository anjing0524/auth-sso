-- ============================================
-- Auth-SSO 数据库初始化脚本
-- ============================================
-- 该脚本在 PostgreSQL 容器首次启动时自动执行
-- IDP 已合并进 Portal，仅需一个数据库
-- ============================================

-- 创建 Portal 统一数据库（含认证 + 业务数据）
CREATE DATABASE auth_sso;

-- 授权 postgres 用户所有权限
GRANT ALL PRIVILEGES ON DATABASE auth_sso TO postgres;

-- 输出创建结果
\echo '============================================'
\echo '数据库创建完成:'
\echo '  - auth_sso (Portal 统一数据库，含 OIDC 认证 + 业务数据)'
\echo '============================================'